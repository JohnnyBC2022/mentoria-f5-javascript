const producto = {
    nombre: "Monitor 20 pulgadas",
    precio: 300,
    disponible: true
}

console.log(producto);

/* ¿Cómo podemos acceder a sus valores */

/* Sintaxis de punto para cada propiedad */

console.log(producto.nombre);
console.log(producto.precio);
console.log(producto.disponible);

/* Forma alternativa (poco usada) */

console.log(producto['nombre']);