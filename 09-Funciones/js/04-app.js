// Diferencia entre funciónes y métodos

const numero1 = 20;
const numero2 = "20";

console.log(parseInt(numero2)) // esto es una función

console.log(numero1.toString()) // esto es un método

// Función:
// Una función es un bloque de código que realiza una tarea específica y puede ser reutilizado.
// Se puede llamar desde cualquier parte del código.
// No está asociada a ningún objeto.

//Método:
// Se llama usando la sintaxis del objeto.
// Un método es una función que pertenece a un objeto.
// Se utiliza para realizar acciones relacionadas con el objeto.