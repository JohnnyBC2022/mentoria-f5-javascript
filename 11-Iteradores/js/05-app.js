// Do..while: El bloque de código se ejecuta una vez antes de verificar la condición

// Útil cuando necesitas asegurar que el bloque de código se ejecute al menos una vez, sin importar la condición inicial.

let i = 0;

do {
    console.log(`Número ${i}`)
    i++; //Incremento
} while (i<10);  // condición
    
